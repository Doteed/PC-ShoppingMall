CREATE SEQUENCE SEQ_QA NOCACHE;

-- FAQ Table
CREATE TABLE FAQ (
    "FAQ_ID" NUMBER PRIMARY KEY, -- FAQ 아이디
    "BOARD_ID" NUMBER NOT NULL,
    "USER_ID" VARCHAR2(30) NOT NULL,
    "AUTH_ID" NUMBER NOT NULL,
    "TITLE" VARCHAR2(300) NOT NULL,
    "CONTENT" VARCHAR2(3000) NOT NULL,
    "DATE" DATE NOT NULL,
